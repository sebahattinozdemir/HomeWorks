/* Arithmetic Operators Example */
#include <stdio.h>

int main()
{
    int a=9, b=4, c;
    c = a+b;
    printf("a+b = %d\n", c);
    c = a-b;
    printf("a-b = %d\n", c);
    c = a*b;
    printf("a*b = %d\n", c);
    c = a/b;
    printf("a/b = %d\n", c);
    c = a%b;
    printf("Remainder when a is divided by b is %d\n", c);
	return 0;
}
